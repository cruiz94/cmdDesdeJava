    private void jBtnMOdifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnMOdifActionPerformed
        String grup="groupmod -n "+ jtxtNuevoGrp.getText()+" "+ jtxtGrupo.getText();
//        JOptionPane.showMessageDialog(this, usuario);
        String s=null;
        try {
            // Ejcutamos el comando
            Process p = Runtime.getRuntime().exec(grup);
            BufferedReader stdInput = new BufferedReader(new InputStreamReader(p.getInputStream()));      
            BufferedReader stdError = new BufferedReader(new InputStreamReader(p.getErrorStream()));
            
            if ((s = stdError.readLine()) != null) {
                JOptionPane.showMessageDialog(this, "Error el Grupo no exite verifique");
            }
            else{
                JOptionPane.showMessageDialog(this, "El Grupo a sido Modificado correctamente");
                GrupoOpciones opciones= new GrupoOpciones();
                opciones.setVisible(true);
                this.setVisible(false);
            }
         }catch (Exception e) {
            System.out.println("Excepci√≥n: ");
            e.printStackTrace();
         }
    }//GEN-LAST:event_jBtnMOdifActionPerformed

